$(document).ready(function() {
    function toggleNav() {
        if ($(window).width() <= 768) {
            $("#sideNav").css("left", "-250px");
        } else {
            $("#sideNav").css("left", "0");
        }
    }
    toggleNav();

    $(window).resize(function() {
        toggleNav();
    });

    $("#open-nav").click(function() {
        $("#sideNav").css("left", "0");
    });

    $("#close-nav").click(function() {
        $("#sideNav").css("left", "-250px");
    });
});

